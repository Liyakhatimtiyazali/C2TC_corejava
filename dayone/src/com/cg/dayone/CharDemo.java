package com.cg.dayone;

public class CharDemo {

	public static void main(String[] args) {
		
		//size of char is 2 bytes
		char letter = 'a';
		System.out.println("The value of letter is " +letter);
		
		char digit = '8';
		System.out.println("The value of digit is " +digit);
		
		char symbol = '$';
		System.out.println("The value of symbol is " +symbol);
		

	}

}
